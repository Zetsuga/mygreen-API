class Incidencias{

    constructor(id_incidencia,id_usuario,id_finca,fecha,estado,descripcion){
        this.id_incidencia = id_incidencia;
        this.id_usuario= id_usuario;
        this.id_finca = id_finca;
        this.fecha = fecha;
        this.estado = estado;
        this.descripcion = descripcion;
    }
}